# La méthode type() permet de donner le type d'une variable!

# Assigne la phrase "Salut voici un texte" a la variable message
message = "Salut voici un texte"

# Affiche message à l'aide d'un print()
print(message)

# Affiche le type de la variable message
print(type(message))

# Écris un message entre les ""
message = "Prénom ici"

# Affiche message à l'aide d'un print()
print(message)
# Assigne à t_msg le type de message
t_msg = type(message)

# Affiche t_msg
print(t_msg)

# variables
